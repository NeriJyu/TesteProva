
public class Calculadora {
	
	
	private int resultado = 0;
	
	public int somar(int n1, int n2) {
		
		resultado = n1 + n2;
		
		return resultado;
	}

	
	public int subtracao(int n1, int n2) {
		resultado = n1 - n2;
		
		return resultado ;
	}
	
	public int multiplicacao(int n1, int n2) {
		resultado = n1 * n2;
		
		return resultado ;
	}
	
	public int divisao(int n1, int n2) {
		resultado = n1 / n2;
		
		return resultado ;
	}

	
	
	

}
